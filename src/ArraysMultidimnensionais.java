public class ArraysMultidimnensionais {
    public static void main(String[] args) {
        String[][] cidade = new String[3][3];
        cidade[0][0] = "Uberlandia";
        cidade[0][1] = "Uberaba";
        cidade[0][2] = "Belo Horizonte";

        cidade[1][0] = "São Paulo";
        cidade[1][1] = "Ribeirão Preto";

        cidade[2][0] = "Fortaleza";

        System.out.println(cidade[0][1]);

    }
}
